# TERMINOLOGÍA:

    # Clase: 
        # Modelo donde se redactaran las caracteristicas comunes de un grupo de objetos
        # Ejemplo un clase auto
    
    # Instancia:
        # Ejemplar, objeto perteneciente a una clase

    # Modularizacion: 
        # Programa compuesto de varias clases. 
        # Cada modulo funciona de forma independiente. 
        # Si una clase o modulo del programa falla, los demas modulos o clases siguen funcionando
        # Solo falla una clase lo que permite que los demas modulos funcionen
    
    # Encapsulamiento:
        # Funcionamiento protegido entre clases.
        # El funcionamiento de una clase desconoce el funcionamiento de otra aunque funcionen
            # de forma conjunta como aplicación completa.
    
    #Metdos de acceso:
        # Permiten conectar a las clases para que funcionen como aplicacion
        # Estos metodos solo tienen acceso a parte de las funciones de la clase
    
